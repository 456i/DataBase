SELECT 
    g.Наименование_товара, 
    ROUND(AVG(g.Цена_продажи), 2) AS [Средняя цена],
    N'Дешёвые товары' AS Категория
FROM Заказы g
WHERE g.Цена_продажи < 100
GROUP BY g.Наименование_товара

UNION ALl


SELECT 
    g.Наименование_товара, 
    ROUND(AVG(g.Цена_продажи), 2),
    N'Дорогие товары'
FROM Заказы g
WHERE g.Цена_продажи >= 100
GROUP BY g.Наименование_товара;