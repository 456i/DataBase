SELECT
	Z.Номер_заказа,
	Z.Наименование_товара,
	Z.Цена_продажи,
	C.Наименование_фирмы
FROM
	Заказы AS Z
	INNER JOIN Заказчики AS C ON Z.Заказчик = C.Наименование_фирмы
WHERE
	Z.Наименование_товара IN (
		SELECT
			Наименование
		FROM
			Товары
		WHERE
			Цена > (
				SELECT
					AVG(Цена)
				FROM
					Товары
			)
	);