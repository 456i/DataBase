SELECT
	p1.Наименование_товара,
	p1.Цена_продажи,
	(
		SELECT
			COUNT(*)
		FROM
			Заказы p2
		WHERE
			p2.Наименование_товара = p1.Наименование_товара
			AND p2.Цена_продажи = p1.Цена_продажи
	) AS [Количество]
FROM
	Заказы p1
GROUP BY
	p1.Наименование_товара,
	p1.Цена_продажи
HAVING
	p1.Цена_продажи < 50
	OR p1.Цена_продажи > 100;